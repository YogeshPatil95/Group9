package Control_Statements;

public class Else_IfStudyPractice {

	public static void main(String[] args) {
		// 
   String city="Kolhapur";
   if(city=="Pune") 
   {
	   System.out.println("Welcome in Pune .");
   }
   else if(city=="Kolhapur") 
   {
	   System.out.println("Welcome in Kolhapur");
	  
   }
   else if(city=="Sangli") {
	   System.out.println("Welcome in Sangli");
   }
   else if (city=="Mumbai") {
	   System.out.println("Welcome in Mumbai");
   }
   else {
	   System.out.println("");
   }
	}

}
